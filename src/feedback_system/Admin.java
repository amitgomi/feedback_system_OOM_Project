    public String selectfac(String faname) throws FileNotFoundException{
        String fuser=" ";
        try {
            File facuser=new File("data/facuser.txt");
            FileReader fileReader = new FileReader(facuser);
            BufferedReader bufferedReader = new BufferedReader(fileReader);
            String line;
            while ((line = bufferedReader.readLine() ) != null) {
                String[] str=line.split("---");
                //System.out.println(faname);
                //System.out.println(str[1]+" sel "+str[0]);
                if(str[1].equals(faname)){
                    //System.out.println("selecting file"+str[0]);
                    fuser = str[0];
                    break;
                }
            }
            fileReader.close();
        } 
        catch (IOException e) {
            e.printStackTrace();
        }
        return fuser;
    }
