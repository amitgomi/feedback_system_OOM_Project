    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String username = this.usrnmeTxt.getText();
        String facname = this.facText.getText();
        String password = String.valueOf(passText.getPassword());
        String confpassword = String.valueOf(confpassText.getPassword());
        if(!password.equals(confpassword)){
            confpassText.setText("");
            JOptionPane.showMessageDialog(rootPane,"both password did not match");
            return ;
        }
        int num=0,alreadyexist = 0;
      
        try {
            File file,file1;
            file1 = new File("data/facuser.txt");
            if(select==1)
                file = new File("data/studentlog.txt");
            else{
                file=new File("data/facultylog.txt");
                
            }
            
            FileReader fileReader = new FileReader(file);
            BufferedReader bufferedReader = new BufferedReader(fileReader);
            String line;
            while ((line = bufferedReader.readLine() ) != null) {
                String[] str=line.split(" ");
                if(str.length<2)
                    continue;
                num = Integer.parseInt(str[0]);
                if(username.equals(str[1])){
                    alreadyexist=1;
                    JOptionPane.showMessageDialog(rootPane, "username already exist");
                    return ;
                }
            }
            
            fileReader.close();
            
            num++;
            FileWriter fw = new FileWriter(file,true);
            fw.append(num+" "+username+" "+password+"\r\n");
            fw.close();
            if(select==2){
                FileWriter fw1 = new FileWriter(file1,true);
                fw1.append(username+".txt---"+facname+"\r\n");
                fw1.close();
            }
        } 
        catch (IOException e) {
            e.printStackTrace();
        }
        this.usrnmeTxt.setText("");
        jPanel2.setVisible(true);
        jPanel3.setVisible(false);
        passText.setText("");
        facText.setText("");
        confpassText.setText("");
    }//GEN-LAST:event_jButton6ActionPerformed

