    public void addfacultylist(){
        ArrayList<String> arr = new ArrayList<String>();
        
        File facuser;
        int count=0;
        try {
            facuser=new File("data/facuser.txt");
            FileReader fileReader = new FileReader(facuser);
            BufferedReader bufferedReader = new BufferedReader(fileReader);
            String line;
            while ((line = bufferedReader.readLine() ) != null) {
                String[] str=line.split("---");
                //System.out.println(faname);
                //System.out.println(str[1]+" sel "+str[0]);
                arr.add(str[1]);
                count++;
            }
            fileReader.close();
        } 
        catch (IOException e) {
            e.printStackTrace();
        }
        Iterator itr = arr.iterator();
        String s[] = new String[count];
        int i;
        for(i=0;i<count;i++){
            s[i]  = (String) itr.next();
        }
        facSelect.setModel(new javax.swing.DefaultComboBoxModel<>(s));
    }
